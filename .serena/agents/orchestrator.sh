# Merge validated improvement
merge_improvement() {
    local section=$1
    local workspace=$2
    local validation_log="$workspace/VALIDATION-LOG.md"

    log "Checking validation status..."

    if [ ! -f "$validation_log" ]; then
        log_error "VALIDATION-LOG.md not found. Run Validator Agent first."
        return 1
    fi

    # Check if validation passed
    if grep -q "Decision:.*PASS" "$validation_log"; then
        log_success "Validation PASSED. Merging improvement to main branch..."

        # Create a new version
        local section_path=$(get_section_info "$section")
        local draft_dir="$LC_DIR/$section_path/versions"
        local latest_version=$(ls -v "$draft_dir" 2>/dev/null | grep -E '^v[0-9]+$' | tail -1)
        local new_version_num=$((${latest_version#v} + 1))
        local new_version_dir="$draft_dir/v$(printf "%02d" $new_version_num)"

        mkdir -p "$new_version_dir"
        cp "$draft_dir/$latest_version/draft.md" "$new_version_dir/draft.md"

        log_success "Created $new_version_dir"
        log "Please review the merged changes before committing to Git."

    elif grep -q "Decision:.*FAIL" "$validation_log"; then
        log_error "Validation FAILED. Improvement not merged."
        log "Please review VALIDATION-LOG.md for details and retry."
        return 1
    else
        log_warning "Validation decision unclear. Please review VALIDATION-LOG.md manually."
        return 1
    fi
}

# Main improvement cycle for one section
run_improvement_cycle() {
    local section=$1
    local max_improvements=5
    local workspace=$(setup_workspace "$section")
    local section_name=$(get_section_name "$section")

    log ""
    log "=========================================="
    log "Starting Improvement Cycle for $section_name"
    log "=========================================="
    log ""

    # Step 1: Manager Agent analysis
    run_manager_agent "$section" "$workspace"

    # Check if improvements were generated
    if [ ! -f "$workspace/IMPROVEMENTS.md" ]; then
        log_error "Manager Agent failed to generate improvements."
        return 1
    fi

    # Steps 2-4: Process each improvement
    for i in $(seq 1 $max_improvements); do
        log ""
        log "------------------------------------------"
        log "Processing Improvement #$i"
        log "------------------------------------------"

        # Worker Agent
        run_worker_agent "$section" "$i" "$workspace"

        # Validator Agent
        run_validator_agent "$section" "$i" "$workspace"

        # Merge if passed
        merge_improvement "$section" "$workspace"

        # Re-run Manager Agent after each merge to reassess
        if [ $i -lt $max_improvements ]; then
            log ""
            log "Re-running Manager Agent to reassess remaining improvements..."
            # In production: re-analyze and update IMPROVEMENTS.md
        fi
    done

    log ""
    log_success "Improvement cycle complete for $section_name!"
    log "Review the versions in: $LC_DIR/$(get_section_info "$section")/versions/"
}

# Show usage
usage() {
    cat << EOF
Autonomous Learning Contract Improvement System - Orchestrator

Usage: $0 [COMMAND] [OPTIONS]

Commands:
    run-all              Run improvement cycle for all 9 sections
    run SECTION           Run improvement cycle for one section (e.g., lc-01)
    manager SECTION       Run Manager Agent only
    worker SECTION NUM    Run Worker Agent for improvement NUM
    validator SECTION NUM Run Validator Agent for improvement NUM
    merge SECTION         Merge validated improvement
    status                Show status of all sections
    clean                 Clean workspace files

Sections:
    lc-01 through lc-09

Examples:
    $0 run lc-01          # Run full cycle for LC:1
    $0 manager lc-01      # Generate improvements for LC:1
    $0 worker lc-01 1     # Implement improvement #1 for LC:1
    $0 validator lc-01 1  # Validate improvement #1 for LC:1
    $0 merge lc-01        # Merge validated improvement for LC:1
    $0 run-all            # Run cycles for all sections

EOF
}

# Show status
show_status() {
    echo ""
    echo "=========================================="
    echo "Autonomous Improvement System Status"
    echo "=========================================="
    echo ""

    for section_info in "${SECTIONS[@]}"; do
        IFS='|' read -r code path words name <<< "$section_info"
        local workspace="$WORKSPACE_DIR/$code"

        echo "$code - $name"

        if [ -d "$workspace" ]; then
            [ -f "$workspace/IMPROVEMENTS.md" ] && echo "  ✓ IMPROVEMENTS.md" || echo "  ✗ IMPROVEMENTS.md (missing)"
            [ -f "$workspace/IMPLEMENTATION-LOG.md" ] && echo "  ✓ IMPLEMENTATION-LOG.md" || echo "  ✗ IMPLEMENTATION-LOG.md (missing)"
            [ -f "$workspace/VALIDATION-LOG.md" ] && echo "  ✓ VALIDATION-LOG.md" || echo "  ✗ VALIDATION-LOG.md (missing)"
        else
            echo "  (not started)"
        fi
        echo ""
    done
}

# Clean workspace
clean_workspace() {
    log "Cleaning workspace..."
    rm -rf "$WORKSPACE_DIR"/*
    log_success "Workspace cleaned."
}

