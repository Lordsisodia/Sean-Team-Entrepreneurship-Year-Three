        run_validator_agent "$2" "$3" "$workspace"
        ;;
    merge)
        if [ -z "${2:-}" ]; then
            log_error "Please specify a section (e.g., lc-01)"
            usage
            exit 1
        fi
        local workspace=$(setup_workspace "$2")
        merge_improvement "$2" "$workspace"
        ;;
    status)
        show_status
        ;;
    clean)
        clean_workspace
        ;;
    *)
        usage
        exit 1
        ;;
esac
